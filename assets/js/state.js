/* ================================================================
   PERSISTENT CROSS-RUN STATE MANAGEMENT
   Word kill counts, lesson progress, vocabulary unlocks
================================================================ */
export function loadPersistentState() {
  // Load word kill tracking
  const savedKillCounts = localStorage.getItem('krr_wordKills');
  if (savedKillCounts) {
    try {
      G.wordKillCounts = JSON.parse(savedKillCounts);
    } catch (e) {
      console.warn('Failed to parse wordKillCounts:', e);
      G.wordKillCounts = {};
    }
  } else {
    G.wordKillCounts = {};
  }

  // Load word hidden status
  const savedHiddenStatus = localStorage.getItem('krr_wordHidden');
  if (savedHiddenStatus) {
    try {
      G.wordHiddenStatus = JSON.parse(savedHiddenStatus);
    } catch (e) {
      console.warn('Failed to parse wordHiddenStatus:', e);
      G.wordHiddenStatus = {};
    }
  } else {
    G.wordHiddenStatus = {};
  }

  // Load word conjugation counts
  const savedConjugationCounts = localStorage.getItem('krr_wordConjugations');
  if (savedConjugationCounts) {
    try {
      G.wordConjugationCounts = JSON.parse(savedConjugationCounts);
    } catch (e) {
      console.warn('Failed to parse wordConjugationCounts:', e);
      G.wordConjugationCounts = {};
    }
  } else {
    G.wordConjugationCounts = {};
  }

  // Load completed lessons
  const savedLessons = localStorage.getItem('krr_completedLessons');
  if (savedLessons) {
    try {
      G.completedLessons = JSON.parse(savedLessons);
    } catch (e) {
      console.warn('Failed to parse completedLessons:', e);
      G.completedLessons = [];
    }
  } else {
    G.completedLessons = [];
  }

  // Load vocabulary threshold
  const savedThreshold = localStorage.getItem('krr_relThreshold');
  G.relThreshold = savedThreshold !== null ? Number(savedThreshold) : 90;

  // Load learned words (all words encountered via kills + lessons)
  const savedLearnedWords = localStorage.getItem('krr_learnedWords');
  if (savedLearnedWords) {
    try {
      G.learnedWords = JSON.parse(savedLearnedWords);
    } catch (e) {
      console.warn('Failed to parse learnedWords:', e);
      G.learnedWords = [];
    }
  } else {
    G.learnedWords = [];
  }

  // Load avatar (Avataaars options)
  const savedAvatar = localStorage.getItem('krr_avatar');
  if (savedAvatar) {
    try { G.avatar = JSON.parse(savedAvatar); } catch(e) { G.avatar = null; }
  }
}

export function savePersistentState() {
  localStorage.setItem('krr_wordKills', JSON.stringify(G.wordKillCounts));
  localStorage.setItem('krr_wordHidden', JSON.stringify(G.wordHiddenStatus));
  localStorage.setItem('krr_wordConjugations', JSON.stringify(G.wordConjugationCounts));
  localStorage.setItem('krr_completedLessons', JSON.stringify(G.completedLessons));
  localStorage.setItem('krr_relThreshold', String(G.relThreshold ?? 90));
  localStorage.setItem('krr_learnedWords', JSON.stringify(G.learnedWords || []));
  if (G.avatar) localStorage.setItem('krr_avatar', JSON.stringify(G.avatar));
}

// Helper: increment word kill count and update hidden status
export function incrementWordKillCount(wordText) {
  if (!G.wordKillCounts[wordText]) {
    G.wordKillCounts[wordText] = 0;
  }
  G.wordKillCounts[wordText]++;
  const count = G.wordKillCounts[wordText];

  // Update hidden status: 50% chance at 5+, always hidden at 10+
  if (count >= 10) {
    G.wordHiddenStatus[wordText] = true;
  } else if (count === 5) {
    G.wordHiddenStatus[wordText] = Math.random() < 0.5;
  }

  savePersistentState();
}

// Helper: increment word conjugation count
export function incrementWordConjugationCount(wordText, conjugationKey) {
  if (!G.wordConjugationCounts[wordText]) {
    G.wordConjugationCounts[wordText] = {};
  }
  if (!G.wordConjugationCounts[wordText][conjugationKey]) {
    G.wordConjugationCounts[wordText][conjugationKey] = 0;
  }
  G.wordConjugationCounts[wordText][conjugationKey]++;
  savePersistentState();
}
